import React, { use } from 'react';
import { MoneyContext } from './Context';

const Brother = () => {
    const [money, setMoney] = use(MoneyContext);
    return (
        <div>
            <h3>Brother</h3>
            <button onClick={() => setMoney(money+1000)}>add 1000 tk</button>
        </div>
    );
};

export default Brother;